#!/bin/bash
#==============================================================================
# Command Wrapper Script with Security Audit
# 御坂妹妹 17 号 - 自动安全拦截包装器
#==============================================================================

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
AUDIT_SCRIPT="${SCRIPT_DIR}/audit-intercept.sh"

#==============================================================================
# 包装器函数
#==============================================================================
secure_exec() {
    local command="$@"
    
    echo "=========================================="
    echo "🛡️  御坂妹妹 17 号 安全审计包装器"
    echo "执行命令：$command"
    echo "=========================================="
    
    # 先进行安全审计
    if ! $AUDIT_SCRIPT "$command" > /dev/null 2>&1; then
        # 审计失败（已拦截）
        echo ""
        echo "❌ 安全审计未通过，命令已被拦截。"
        echo "请查看御坂妹妹 17 号发送的警报消息获取详细信息。"
        return 127
    fi
    
    # 审计通过，执行命令
    echo ""
    echo "✅ 安全审计通过，正在执行命令..."
    echo "------------------------------------------"
    
    # 执行原始命令
    eval "$command"
    local exit_code=$?
    
    if [ $exit_code -eq 0 ]; then
        echo "------------------------------------------"
        echo "✅ 命令执行成功"
    else
        echo "------------------------------------------"
        echo "⚠️  命令执行失败，退出码：$exit_code"
    fi
    
    return $exit_code
}

#==============================================================================
# 如果直接运行此脚本
#==============================================================================
if [[ "${BASH_SOURCE[0]}" == "${0}" ]]; then
    if [ $# -eq 0 ]; then
        echo "用法：$0 <命令>"
        echo ""
        echo "示例："
        echo "  $0 'ls -la /tmp'"
        echo "  $0 'git status'"
        echo "  $0 'rm -rf /tmp/test'  # 会被拦截"
        echo ""
        echo "此包装器会自动对命令进行安全审计，"
        echo "御坂妹妹 17 号 会拦截高危操作！"
        exit 1
    fi
    
    secure_exec "$@"
fi
